    private void insert1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insert1ActionPerformed
        // TODO add your handling code here:
        String serverUrl = "http://localhost:80/my-site/insert.php";

        // Sample data to be inserted into the database
        String studentName=name.getText();
        String studentGrade =grade.getText();
        String selectedCity = (String) city.getSelectedItem();

        try {
            URL url = new URL(serverUrl);
            HttpURLConnection connection = (HttpURLConnection) url.openConnection();

            // Set the request method (POST)
            connection.setRequestMethod("POST");
            connection.setDoOutput(true);

            // Send the data to the PHP server
            String data = "name=" + studentName + "&grade=" + studentGrade + "&city=" + selectedCity;
            OutputStream outputStream = connection.getOutputStream();
            outputStream.write(data.getBytes());
            outputStream.flush();
            outputStream.close();

            // Get the response code
            int responseCode = connection.getResponseCode();

            if (responseCode == HttpURLConnection.HTTP_OK) {
                // Read the response from the PHP server (optional)
                // You can read the response if the server sends any data back
            } else {
                System.out.println("HTTP request failed: " + responseCode);
            }

            // Close the connection
            connection.disconnect();
        } catch (IOException e) {
            e.printStackTrace();
        }  
    }//GEN-LAST:event_insert1ActionPerformed

