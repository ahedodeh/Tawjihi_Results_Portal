    private void login(String email, String password) {
    try {
        // Create the URL object with the query parameters
        String urlString = "http://localhost:80/my-site/login.php";
        urlString += "?email=" + URLEncoder.encode(email, "UTF-8");
        urlString += "&password=" + URLEncoder.encode(password, "UTF-8");
        URL url = new URL(urlString);

        // Open the connection
        HttpURLConnection connection = (HttpURLConnection) url.openConnection();

        // Set the request method (GET)
        connection.setRequestMethod("GET");

        // Get the response code
        int responseCode = connection.getResponseCode();

        if (responseCode == HttpURLConnection.HTTP_OK) {
            // Read the response from the PHP server
            try (BufferedReader in = new BufferedReader(new InputStreamReader(connection.getInputStream()))) {
                String response = in.readLine();

                System.out.println("Response from PHP Server: " + response);

                // Process the response
                if (response.equals("Success")) {
                    JOptionPane.showMessageDialog(this, "Login successful!");
                    EventQueue.invokeLater(() -> {
                        insertPage storeFrame = new insertPage();
                        storeFrame.setVisible(true);
                        dispose();
                    });
                } else {
                    // Admin login failed
                    JOptionPane.showMessageDialog(this, "Invalid credentials. Please try again.");
                }
            }
        } else {
            System.out.println("HTTP request failed: " + responseCode);
        }

        // Close the connection
        connection.disconnect();
    } catch (IOException ex) {
        ex.printStackTrace();
    }


}
